# Inicio del programa
import math
# Paso 1: Solicitar al usuario que ingrese el radio del círculo
radio = int(input("Por favor, Ingrese el radio del círuclo: "))

# Paso 2: Calcular el área del círculo utilizando la fórmula area = pi * radio^2
area = math.pi * (radio**2)

# Paso 3: Mostrar al usuario el área calculada
print("El area del ciruclo con radio", radio, "es: ", area)

